namespace CodingTrackerConsoleApp;

/// <summary>
/// Represents a coding session.
/// </summary>
public class CodingSession
{
    /// <summary>
    /// Gets or sets the start time of the coding session.
    /// </summary>
    public DateTime StartTime { get; set; }

    /// <summary>
    /// Gets or sets the end time of the coding session.
    /// </summary>
    public DateTime EndTime { get; set; }
}
